var mysqlCount int

func mysqlDummyReader(c io.Reader) (buf []byte, err error) {
	if mysqlCount < len(sampleMySQL) {
		buf = sampleMySQL[mysqlCount]
		mysqlCount++
	} else {
		err = errors.New("EOF")
	}
	return
}

func TestMySQL(t *testing.T) {
	var m = dbms.MySQL{}
	port := m.DefaultPort()
	if m.DefaultPort() != 3306 {
		t.Error("Expected 3306, got ", port)
	}
	err := m.SetCertificate("", "")
	if err == nil {
		t.Error("Expected error")
	}
	m.SetReader(mysqlDummyReader)
	var s mock.ConnMock
	m.SetSockets(s, s)
	err = m.Handler()
	if err != nil {
		t.Error("Got error", err)
	}
	m.Close()
}

func BenchmarkMySQL(b *testing.B) {
	b.ReportAllocs()
	b.ResetTimer()
	var s mock.ConnMock
	var m = dbms.MySQL{}
	m.SetReader(mysqlDummyReader)
	m.SetSockets(s, s)
	for i := 0; i < b.N; i++ {
		mysqlCount = 0
		err := m.Handler()
		if err != nil {
			b.Fatal(err)
		}
		m.Close()
	}
}
